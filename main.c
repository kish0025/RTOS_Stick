/* USER CODE END Header_StartTask1 */
void StartTask1(void *argument)
{
    while (1)
    {

    	HAL_ADC_Start(&hadc1);
    	HAL_ADC_PollForConversion(&hadc1,20);
    	lux = HAL_ADC_GetValue(&hadc1);

    	if(lux>4000){
    		printf("The Stick has Fallen, Emergency\n");
    	}

        if (emergencyFlag)
        {
            printf("Task2: Emergency triggered!\r\n");
            emergencyFlag = 0;  // Reset emergency flag after handling it
        }
        else
        {
            printf("Task2 - Movement \r\n");
        }

        osDelay(1000); // Adjust the delay as needed
    }
}

void StartTask2(void *argument)




{
    uint32_t numTicks = 0;

    while (1)
    {
        // Set TRIG to LOW for few uSec
        HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_RESET);
        usDelay(3);

        // START Ultrasonic measure routine
        HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_SET); // Output 10 usec TRIG
        usDelay(10);
        HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_RESET);

        // Wait for ECHO pin rising edge
        while(HAL_GPIO_ReadPin(ECHO_GPIO_Port, ECHO_Pin) == GPIO_PIN_RESET);

        // Start measuring ECHO pulse width
        numTicks = 0;
        while(HAL_GPIO_ReadPin(ECHO_GPIO_Port, ECHO_Pin) == GPIO_PIN_SET)
        {
            numTicks++;
            usDelay(2); // 2.8us
        };

        // Estimate distance in cm
        distance = (numTicks + 0.0f) * 2.8 * speedOfSound;

        // Emergency message if distance is greater than 7 cm

       // printf("The Distance is : ")
        printf("Task1- Distance \n");
        if (distance < 10.0f)
        {
            emergencyFlag = 1;  // Set emergency flag
            printf("Object detected!\r\n");  // Print emergency message
        }

        HAL_Delay(100);
        osDelay(1000); // Ensure other tasks have a chance to run
    }
}

















